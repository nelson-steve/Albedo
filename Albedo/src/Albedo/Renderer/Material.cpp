#include "AlbedoPreCompiledHeader.h"

#include "Material.h"

namespace Albedo {

	glm::vec3 MaterialData::LightPos = { 0.0f, 6.0f, 0.0f };

	Material::Material()
	{
		Albedo_Core_INFO("Material: Constructor called");
	}

	Material::~Material()
	{
		Albedo_Core_INFO("Material: Destructor called");
	}

	void Material::Init(MaterialType type)
	{
		m_Type = type;

		float skyboxVertices[] =
		{
			-1.0f,  1.0f, -1.0f,
			-1.0f, -1.0f, -1.0f,
			 1.0f, -1.0f, -1.0f,
			 1.0f, -1.0f, -1.0f,
			 1.0f,  1.0f, -1.0f,
			-1.0f,  1.0f, -1.0f,

			-1.0f, -1.0f,  1.0f,
			-1.0f, -1.0f, -1.0f,
			-1.0f,  1.0f, -1.0f,
			-1.0f,  1.0f, -1.0f,
			-1.0f,  1.0f,  1.0f,
			-1.0f, -1.0f,  1.0f,

			 1.0f, -1.0f, -1.0f,
			 1.0f, -1.0f,  1.0f,
			 1.0f,  1.0f,  1.0f,
			 1.0f,  1.0f,  1.0f,
			 1.0f,  1.0f, -1.0f,
			 1.0f, -1.0f, -1.0f,

			-1.0f, -1.0f,  1.0f,
			-1.0f,  1.0f,  1.0f,
			 1.0f,  1.0f,  1.0f,
			 1.0f,  1.0f,  1.0f,
			 1.0f, -1.0f,  1.0f,
			-1.0f, -1.0f,  1.0f,

			-1.0f,  1.0f, -1.0f,
			 1.0f,  1.0f, -1.0f,
			 1.0f,  1.0f,  1.0f,
			 1.0f,  1.0f,  1.0f,
			-1.0f,  1.0f,  1.0f,
			-1.0f,  1.0f, -1.0f,

			-1.0f, -1.0f, -1.0f,
			-1.0f, -1.0f,  1.0f,
			 1.0f, -1.0f, -1.0f,
			 1.0f, -1.0f, -1.0f,
			-1.0f, -1.0f,  1.0f,
			 1.0f, -1.0f,  1.0f
		};

		float cubeVertices[8 * 3]
		{
			-0.5f, -0.5f,  0.5f,
			-0.5f,  0.5f,  0.5f,
			 0.5f,  0.5f,  0.5f,
			 0.5f, -0.5f,  0.5f,

			-0.5f, -0.5f, -0.5f,
			-0.5f,  0.5f, -0.5f,
			 0.5f,  0.5f, -0.5f,
			 0.5f, -0.5f, -0.5f
		};

		switch (type)
		{
			case Albedo::MaterialType::Skybox:
				m_MaterialData.VertexData = skyboxVertices;
				break;
			case Albedo::MaterialType::Cube:
				m_MaterialData.VertexData = cubeVertices;
				break;
			case Albedo::MaterialType::Model:
				break;
			case Albedo::MaterialType::Sprite:
				break;
			case Albedo::MaterialType::Sphere:
				break;
			case Albedo::MaterialType::Line:
				break;
		}

	}

}